#Somando e Subtraindo 
y = int(input("Começaremos na soma, então digite um número: "))
z = int(input("Continuando na soma, digite outro número: "))
soma = y + z 
print(f"O resultado final é: {soma}")

a = int(input("Na subtração, digite um número: "))
b = int(input("Continuando na subtração digite mais um número: "))
subtração = a - b
print(f"A subtração final é: {subtração}")
